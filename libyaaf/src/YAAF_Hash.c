#include "YAAF_Hash.h"
#include <ctype.h>

/* One At a Time Hash (http://www.burtleburtle.net/bob/hash/doobs.html)
   tailored for case insenstive strings */
uint32_t
YAAF_OnceAtATimeHashNoCase(const char* str)
{
    uint32_t hash = 0;

    for(;*str; ++str)
    {
        hash += tolower(*str);
        hash += (hash << 10);
        hash ^= (hash >> 6);
    }
    hash += (hash << 3);
    hash ^= (hash >> 11);
    hash += (hash << 15);

    return hash;
}
